//! Common types used across Tamandua GUI and core library.
//!
//! These types are shared between the agent, GUI, and backend.

use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use uuid::Uuid;

// ============================================================================
// Alert Types
// ============================================================================

/// Severity level of an alert
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum AlertSeverity {
    /// Critical severity - immediate action required
    Critical,
    /// High severity - action required soon
    High,
    /// Medium severity - should be investigated
    Medium,
    /// Low severity - informational
    Low,
    /// Informational - no action required
    Info,
}

impl Default for AlertSeverity {
    fn default() -> Self {
        Self::Info
    }
}

/// Status of an alert
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum AlertStatus {
    /// New alert, not yet reviewed
    New,
    /// Alert is being investigated
    Investigating,
    /// Alert has been resolved
    Resolved,
    /// Alert was a false positive
    FalsePositive,
    /// Alert has been dismissed
    Dismissed,
}

impl Default for AlertStatus {
    fn default() -> Self {
        Self::New
    }
}

/// An alert generated by the detection engine
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Alert {
    /// Unique alert ID
    pub id: Uuid,
    /// Alert title
    pub title: String,
    /// Alert description
    pub description: String,
    /// Severity level
    pub severity: AlertSeverity,
    /// Current status
    pub status: AlertStatus,
    /// Source of the alert (e.g., "YARA", "Sigma", "ML")
    pub source: String,
    /// MITRE ATT&CK technique IDs
    pub mitre_techniques: Vec<String>,
    /// Affected process ID (if applicable)
    pub process_id: Option<u32>,
    /// Affected file path (if applicable)
    pub file_path: Option<String>,
    /// Agent ID that generated the alert
    pub agent_id: String,
    /// Timestamp when alert was created
    pub created_at: DateTime<Utc>,
    /// Timestamp when alert was last updated
    pub updated_at: DateTime<Utc>,
    /// Additional metadata
    pub metadata: serde_json::Value,
}

impl Alert {
    /// Create a new alert
    pub fn new(
        title: impl Into<String>,
        description: impl Into<String>,
        severity: AlertSeverity,
    ) -> Self {
        let now = Utc::now();
        Self {
            id: Uuid::new_v4(),
            title: title.into(),
            description: description.into(),
            severity,
            status: AlertStatus::New,
            source: String::new(),
            mitre_techniques: Vec::new(),
            process_id: None,
            file_path: None,
            agent_id: String::new(),
            created_at: now,
            updated_at: now,
            metadata: serde_json::Value::Null,
        }
    }
}

/// Filter criteria for querying alerts
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct AlertFilter {
    /// Filter by severity levels
    pub severities: Option<Vec<AlertSeverity>>,
    /// Filter by status
    pub statuses: Option<Vec<AlertStatus>>,
    /// Filter by source
    pub sources: Option<Vec<String>>,
    /// Filter by agent ID
    pub agent_id: Option<String>,
    /// Filter by date range start
    pub from_date: Option<DateTime<Utc>>,
    /// Filter by date range end
    pub to_date: Option<DateTime<Utc>>,
    /// Search in title/description
    pub search: Option<String>,
    /// Maximum number of results
    pub limit: Option<usize>,
    /// Offset for pagination
    pub offset: Option<usize>,
}

