/// Type of event notification
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum EventNotification {
    /// New alert generated
    Alert(Alert),
    /// Scan progress update
    ScanProgress {
        scan_id: Uuid,
        progress: u8,
        files_scanned: u64,
    },
    /// Scan completed
    ScanComplete(ScanResult),
    /// Agent status changed
    StatusChanged {
        agent_id: String,
        old_status: crate::AgentStatus,
        new_status: crate::AgentStatus,
    },
    /// System metrics update
    Metrics(SystemMetrics),
    /// Threat detected
    ThreatDetected {
        threat: ThreatInfo,
        auto_quarantined: bool,
    },
    /// Configuration updated
    ConfigUpdate {
        agent_id: String,
        config_version: u32,
    },
    /// Connection state changed
    ConnectionState {
        connected: bool,
        server_url: Option<String>,
    },
    /// Error occurred
    Error {
        code: String,
        message: String,
        details: Option<serde_json::Value>,
    },
}

