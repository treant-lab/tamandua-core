/// Type of scan to perform
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ScanType {
    /// Quick scan - critical areas only
    Quick,
    /// Full system scan
    Full,
    /// Custom scan with specific paths
    Custom,
    /// Memory scan
    Memory,
    /// Network scan
    Network,
    /// Registry scan (Windows only)
    Registry,
}

impl Default for ScanType {
    fn default() -> Self {
        Self::Quick
    }
}

/// Status of a scan operation
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ScanStatus {
    /// Scan is pending
    Pending,
    /// Scan is in progress
    Running,
    /// Scan completed successfully
    Completed,
    /// Scan was cancelled
    Cancelled,
    /// Scan failed with error
    Failed,
}

impl Default for ScanStatus {
    fn default() -> Self {
        Self::Pending
    }
}

/// Result of a scan operation
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ScanResult {
    /// Unique scan ID
    pub id: Uuid,
    /// Type of scan performed
    pub scan_type: ScanType,
    /// Current status
    pub status: ScanStatus,
    /// Progress percentage (0-100)
    pub progress: u8,
    /// Number of files scanned
    pub files_scanned: u64,
    /// Number of threats found
    pub threats_found: u32,
    /// Number of items quarantined
    pub items_quarantined: u32,
    /// List of detected threats
    pub threats: Vec<ThreatInfo>,
    /// Scan start time
    pub started_at: DateTime<Utc>,
    /// Scan end time (if completed)
    pub completed_at: Option<DateTime<Utc>>,
    /// Error message (if failed)
    pub error: Option<String>,
    /// Custom paths (for custom scans)
    pub custom_paths: Vec<String>,
}

impl ScanResult {
    /// Create a new scan result
    pub fn new(scan_type: ScanType) -> Self {
        Self {
            id: Uuid::new_v4(),
            scan_type,
            status: ScanStatus::Pending,
            progress: 0,
            files_scanned: 0,
            threats_found: 0,
            items_quarantined: 0,
            threats: Vec::new(),
            started_at: Utc::now(),
            completed_at: None,
            error: None,
            custom_paths: Vec::new(),
        }
    }
}

/// Information about a detected threat
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ThreatInfo {
    /// Threat name/identifier
    pub name: String,
    /// Detection method (YARA rule, ML model, etc.)
    pub detection_method: String,
    /// Affected file path
    pub file_path: String,
    /// Threat severity
    pub severity: AlertSeverity,
    /// Whether the threat was quarantined
    pub quarantined: bool,
    /// SHA256 hash of the file
    pub file_hash: Option<String>,
    /// Detection timestamp
    pub detected_at: DateTime<Utc>,
}

